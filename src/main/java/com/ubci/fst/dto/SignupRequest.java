package com.ubci.fst.dto;
 
public class SignupRequest {

    private String email;

    private String nom;

 
    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    
    public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

 
}